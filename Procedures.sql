--STORED PROCEDURES FOR ORDERS
CREATE PROCEDURE ADDORDERS
@O_ID VARCHAR(10),
@P_ID VARCHAR(10),
@C_ID VARCHAR(10),
@O_QTY INT 
AS
BEGIN
     INSERT INTO ORDERS (O_ID, O_DATE, P_ID, C_ID, O_QTY)
	 VALUES (@O_ID, GETDATE(), @P_ID, @C_ID, @O_QTY);
	 SELECT * FROM ORDERS WHERE O_ID = @O_ID;
END;

