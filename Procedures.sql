--STORED PROCEDURES FOR SUPPLIERS
CREATE PROCEDURE ADDSUPPLIER
@S_ID VARCHAR(10),
@S_NAME VARCHAR(50), 
@S_ADD VARCHAR(100),
@S_CITY VARCHAR(50),
@S_PHONE VARCHAR(15),
@EMAIL VARCHAR(100)
AS 
BEGIN
     INSERT INTO SUPPLIER
     VALUES (@S_ID, @S_NAME, @S_ADD, @S_CITY, @S_PHONE, @EMAIL);
	 SELECT * FROM SUPPLIER WHERE S_ID = @S_ID;
END;

